package assignment2;

public class for_loop {
    public static void main(String[] args) {
        int count =0;
        for (int i = 1; i <= 5; i++) {

            for (int j = 1; j <= 5; j++) {

                System.out.println(j);
                count++;


            }

        }
        System.out.println("This the count:"+count);
    }
}
